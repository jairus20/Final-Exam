package CapaNegocio;

public class Empleado {

    private Object codigo;

    private Object dni;

    private Object nombre;

    private Object disponibilidad;

    public Object getCodigo() {
        return codigo;
    }

    public void setCodigo(Object codigo) {
        this.codigo = codigo;
    }

    public Object getDni() {
        return dni;
    }

    public void setDni(Object dni) {
        this.dni = dni;
    }

    public Object getNombre() {
        return nombre;
    }

    public void setNombre(Object nombre) {
        this.nombre = nombre;
    }

    public Object getDisponibilidad() {
        return disponibilidad;
    }

    public void setDisponibilidad(Object disponibilidad) {
        this.disponibilidad = disponibilidad;
    }
    
}
